public class Printing_Msg3 {
    public static void main(String[] args)
    {
        //Here \t is used for tab purpose which gives space.It gives tab between two words.
        //Here \n is used for next line.Control goes to next line.
        System.out.println("\n\nThe");
        System.out.println("\t Cake");
        System.out.print("Is");//We used print not println because we want to print 'A' in same line.
        System.out.println("\t\t  A");
        System.out.println("\t Lie");
    }
}
